  <h2>¿Por qué no tengo que dar los datos del negocio?</h2>
  <p>Porque el QR ya identifica al negocio: cuando lo escaneas, el sistema sabe de qué comercio viene el ticket y usa la información fiscal que ese negocio subió una sola vez a su cuenta. Tú nunca escribes el RFC ni la razón social del negocio; solo confirmas los tuyos. Es la diferencia entre facturar ticket sin datos del negocio y tener que buscar esa información en un ticket arrugado.</p>

  <h2>Errores comunes al facturar un ticket (y cómo evitarlos)</h2>
  <ul>
    <li><strong>RFC mal escrito.</strong> Revísalo antes de enviarlo; una letra de más es motivo suficiente para que la factura salga rechazada.</li>
    <li><strong>Foto ilegible.</strong> Estira el ticket sobre una superficie plana y busca buena luz antes de tomar la foto; evita sombras y reflejos sobre el folio y el total.</li>
    <li><strong>Ticket ya facturado.</strong> Cada folio se factura una sola vez, así que si ya generaste tu CFDI para ese ticket no podrás duplicarlo — revisa tu correo antes de intentarlo de nuevo.</li>
    <li><strong>Correo mal escrito.</strong> Si tu factura no llega en un par de minutos, revisa spam y confirma que el correo quedó bien escrito antes de reportar el problema.</li>
    <li><strong>Esperar demasiado.</strong> La ventana para facturar depende de la política de cada negocio y del periodo fiscal en el que compraste. Lo más seguro es facturar el mismo día; ante cualquier duda de tu caso particular, pregunta al negocio o a tu contador.</li>
  </ul>

