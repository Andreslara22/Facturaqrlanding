  <div class="tldr"><strong>Respuesta rápida:</strong> Es la versión vigente de la factura electrónica en México: un comprobante fiscal digital (PDF y XML) timbrado por un PAC autorizado y válido ante el SAT, que incluye datos como RFC, régimen fiscal y uso del CFDI del receptor.</div>

  <p>Un cliente te pide "su factura" y tú piensas: ¿cuál versión, qué datos, qué es eso del CFDI 4.0 que menciona todo el mundo? No estás solo. La mayoría de los dueños de negocio conoce la palabra, pero nadie se las explicó fácil.</p>

  <p>Aquí va, sin tecnicismos, para que sepas exactamente qué le estás dando a tu cliente cuando facturas un ticket.</p>

  <h2>¿Qué es el CFDI 4.0, en palabras simples?</h2>
  <p>CFDI significa Comprobante Fiscal Digital por Internet. Es, ni más ni menos, la factura electrónica oficial en México. El "4.0" es simplemente la versión más reciente de ese formato, la que el SAT exige usar desde 2022.</p>
  <p>Piénsalo como una credencial de elector: existe una versión anterior y una vigente, y solo la vigente es válida hoy. El CFDI 4.0 es la versión vigente. Si alguien te ofrece facturar en 3.3, ya no aplica.</p>
  <p>Cada CFDI 4.0 se compone de dos archivos que siempre van juntos:</p>
  <ul>
    <li><strong>Un XML</strong>, el archivo "oficial" que el SAT reconoce y valida.</li>
    <li><strong>Un PDF</strong>, la versión bonita y legible que tu cliente puede imprimir o guardar para verla fácil.</li>
  </ul>

  <h2>¿Para qué sirve el CFDI 4.0?</h2>
  <p>Para tu cliente, sirve como comprobante formal de que gastó ese dinero en tu negocio. Con ese comprobante puede, según su caso, deducir el gasto o simplemente tener un respaldo ordenado de sus compras.</p>
  <p>Para ti como negocio, facturar correctamente es lo que la ley pide cuando alguien lo solicita. Y hacerlo bien evita el dolor de cabeza de una factura rechazada, cancelada o mal hecha que tu cliente te regresa un mes después pidiendo que la corrijas.</p>

  <h2>¿Qué diferencia hay entre el CFDI 3.3 y el 4.0?</h2>
  <p>La diferencia principal está en los datos que pide del receptor (o sea, de tu cliente). El 4.0 es más estricto porque exige que la información coincida exactamente con lo que el SAT tiene registrado:</p>
  <ul>
    <li>Nombre o razón social igual que en la constancia de situación fiscal.</li>
    <li>Código postal del domicilio fiscal registrado.</li>
    <li>Régimen fiscal del receptor.</li>
    <li>Uso que se le dará al CFDI (por ejemplo, gastos en general o adquisición de mercancías).</li>
  </ul>
  <p>¿Por qué el cambio? Porque antes era común que la factura no coincidiera exactamente con los datos del SAT y terminara rechazada o generando problemas al declarar. El 4.0 busca que eso pase menos.</p>
  <p>La buena noticia: tú, como negocio que emite la factura, no tienes que memorizar esta lista. Un sistema de facturación bien hecho ya la valida por ti antes de timbrar.</p>

