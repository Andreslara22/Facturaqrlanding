  <h2>¿Qué necesitas para que te hagan una factura CFDI 4.0?</h2>
  <p>Del lado del cliente que pide la factura, los datos básicos son:</p>
  <ul>
    <li><strong>RFC</strong> (persona física o moral).</li>
    <li><strong>Nombre o razón social</strong>, tal cual está en el SAT.</li>
    <li><strong>Código postal</strong> de su domicilio fiscal registrado.</li>
    <li><strong>Régimen fiscal</strong> bajo el que tributa.</li>
    <li><strong>Uso del CFDI</strong>, es decir, para qué usará esa factura.</li>
  </ul>
  <p>Del lado del negocio que emite, se necesita el propio RFC y régimen fiscal, además de un Certificado de Sello Digital (CSD) vigente ante el SAT, que es lo que "firma" cada factura. Ese CSD se sube una sola vez a tu sistema de facturación y queda listo para timbrar.</p>
  <p>Si tienes dudas sobre tu régimen fiscal o qué CSD te corresponde, ese es terreno de tu contador o del propio SAT: ellos te lo confirman según tu situación exacta.</p>

  <h2>¿Qué es el XML y para qué sirve además del PDF?</h2>
  <p>Aquí hay una confusión común: mucha gente cree que el PDF es "la factura". En realidad, el PDF es solo la versión para leer con los ojos. El archivo que de verdad tiene valor fiscal es el XML.</p>
  <p>El XML lleva dentro el sello digital del PAC (Proveedor Autorizado de Certificación) que timbró esa factura, además de todos los datos en un formato que los sistemas contables y el SAT pueden validar automáticamente. Sin ese sello, no hay CFDI 4.0 válido, solo un documento con apariencia de factura.</p>
  <p>Por eso, cuando factures un ticket, asegúrate de que tu cliente reciba los dos archivos: PDF para ver y XML para guardar y, si le hace falta, entregarlo a su contador.</p>

  <h2>El timbrado: el paso que hace válida tu factura</h2>
  <p>"Timbrar" es el proceso por el cual un PAC autorizado por el SAT revisa tu CFDI, le pone su sello digital y lo registra. Sin ese sello, tu factura no existe fiscalmente, aunque tenga toda la información correcta.</p>
  <p>Piensa en el PAC como el notario de tu factura: no importa qué tan bien redactado esté el documento, si el notario no lo sella, no es válido ante la autoridad. Ese sellado ocurre en segundos y es invisible para tu cliente: él solo ve que su factura llegó a su correo.</p>

