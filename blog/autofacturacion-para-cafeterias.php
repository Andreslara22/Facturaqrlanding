  <div class="tldr"><strong>Respuesta rápida:</strong> Pega un <strong>cartel QR</strong> junto a tu caja: tu cliente lo escanea, toma foto de su ticket y recibe su <strong>CFDI 4.0</strong> por correo en menos de un minuto. Tu barista o panadero no captura RFCs, no hay fila de facturación y funciona igual en local fijo o food truck.</div>

  <p>En una cafetería, el momento más caro del día es la hora pico. Y justo ahí aparece el cliente godín que necesita factura de sus $87 del latte y el croissant... con la fila creciendo atrás.</p>
  <p>Para un negocio de alimentos con ticket promedio bajo, cada minuto de caja vale oro. Capturar un RFC a mano puede tomar más tiempo que preparar la bebida. La autofacturación existe exactamente para eso.</p>

  <h2>Por qué el ticket bajo hace la factura más dolorosa</h2>
  <ul>
    <li><strong>La factura de $87 cuesta lo mismo que la de $8,700:</strong> mismos 5 minutos de captura, mismos errores de RFC, misma fila.</li>
    <li><strong>Muchos clientes de oficina:</strong> cafeterías y panaderías cerca de corporativos viven de clientes que SÍ o SÍ necesitan facturar (viáticos, caja chica).</li>
    <li><strong>Personal rotativo:</strong> capacitar a cada barista nuevo en el sistema de facturación es tiempo perdido; con el QR, no hay nada que capacitar.</li>
  </ul>

  <h2>Cómo se ve el flujo en tu barra</h2>
  <ol>
    <li><strong>Cliente:</strong> "¿Me puede dar factura?"</li>
    <li><strong>Barista:</strong> "Claro, escanee el QR del mostrador con su ticket." (Fin de su participación.)</li>
    <li>El cliente escanea, la IA lee el folio y el total de la foto del ticket, él captura sus datos fiscales una sola vez (o los llena con la foto de su Constancia) y su factura llega a su correo con PDF y XML.</li>
  </ol>
  <p>La fila nunca se detuvo. Y si el cliente tiene prisa, puede facturar después: los tickets valen hasta 3 meses.</p>

