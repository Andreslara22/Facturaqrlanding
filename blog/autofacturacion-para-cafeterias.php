  <h2>¿Y si tengo food truck o varias sucursales?</h2>
  <p><strong>Food truck:</strong> el QR va en la ventanilla, en el menú o impreso en el ticket. Todo pasa en el celular del cliente, así que da igual dónde estés estacionado hoy.</p>
  <p><strong>Varias sucursales:</strong> cada sucursal puede tener su propio portal y QR —con el mismo RFC o RFCs distintos— y las facturas quedan separadas por sucursal en tu panel. Si compartes un solo QR, el cliente elige la sucursal antes de facturar.</p>

