  <h2>En tu gasolinera, nadie se baja a facturar</h2>
  <p>Una gasolinera vive de la velocidad. El cliente llega, carga, paga y se va. No hay tiempo para que el despachador capture RFC, régimen fiscal y uso de CFDI mientras hay tres autos más esperando turno.</p>
  <p>Aun así, muchos de tus clientes necesitan factura: choferes de flotillas, repartidores, empresas de transporte, gente de paso en un viaje de trabajo. Si no facturas rápido, terminas pidiendo que manden el ticket por correo o apuntando datos a mano en la caja, y eso frena la fila.</p>
  <p>La facturación para gasolineras con QR resuelve justo ese cuello de botella: el cliente se factura solo, con su propio celular, sin que nadie de tu estación toque una sola tecla.</p>
  <h2>¿Por qué facturar gasolina es distinto a facturar en cualquier otro negocio?</h2>
  <p>Porque el volumen es altísimo y el tiempo de contacto con el cliente es de segundos. En un restaurante el cliente se queda sentado un rato; en tu gasolinera, apenas se detiene lo necesario para pagar.</p>
  <p>Además, buena parte de tus clientes son choferes que no traen consigo los datos fiscales de la empresa para la que trabajan. Necesitan un momento para buscar el RFC correcto, y ese momento casi nunca es en la bomba.</p>
  <p>Por eso el QR de FacturaQR no obliga a facturar ahí mismo. El cliente puede escanearlo, guardar el enlace de su ticket y facturar más tarde, cuando tenga los datos a la mano.</p>
  <h2>Cómo facturar un ticket de gasolinera con una foto</h2>
  <p>El proceso es el mismo que ya usan restaurantes y tiendas, adaptado al ritmo de una estación de servicio:</p>
  <ol>
    <li><strong>Escanea el QR</strong> — puede estar pegado en la bomba, impreso en el ticket o en un cartel en la tienda de conveniencia.</li>
    <li><strong>Toma foto de su ticket</strong> — la IA lee folio, fecha, litros y total al instante, sin que nadie los transcriba.</li>
    <li><strong>Escribe su RFC y correo</strong> — y recibe su CFDI 4.0, PDF y XML timbrados, en menos de un minuto.</li>
  </ol>
  <p>El despachador sigue despachando. La caja sigue cobrando. Nadie de tu personal se convierte en capturista de facturas.</p>
  <h2>No solo es la carga: también la tienda de conveniencia</h2>
  <p>En muchas estaciones, el ticket de la tienda —café, snacks, aceite, refacciones menores— también necesita factura. El mismo QR sirve para eso: el cliente escanea, sube la foto del ticket que traiga y listo, sin importar si fue en la bomba o en el mostrador.</p>
  <p>Eso simplifica todo para tu equipo. No hay que explicar "para gasolina es un proceso y para la tienda es otro". Es un solo QR, un solo flujo, para cualquier ticket que salga de tu caja.</p>
