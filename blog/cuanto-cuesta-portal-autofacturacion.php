  <h2>Cómo calcular lo que la facturación manual te cuesta hoy</h2>
  <p>Antes de comparar mensualidades, calcula tu costo actual, porque facturar a mano no es gratis:</p>
  <ul>
    <li><strong>Tiempo de caja:</strong> 5 minutos por factura × 100 facturas al mes ≈ 8 horas de tu cajero capturando RFCs.</li>
    <li><strong>Errores y refacturaciones:</strong> cada RFC mal capturado es una factura cancelada, un cliente molesto y tiempo doble.</li>
    <li><strong>Filas y ventas perdidas:</strong> en hora pico, la persona que pide factura detiene a los que vienen atrás.</li>
  </ul>
  <p>Con ese número en mano, la pregunta cambia de "¿cuánto cuesta el portal?" a "¿cuánto me cuesta NO tenerlo?".</p>

  <h2>Checklist para comparar proveedores</h2>
  <ul>
    <li>¿Cuántas <strong>facturas al mes</strong> incluye el precio? ¿Qué pasa si me paso?</li>
    <li>¿Los <strong>timbres del PAC</strong> van incluidos o se cobran aparte?</li>
    <li>¿El portal lleva <strong>mi logo y mi marca</strong> o la del proveedor?</li>
    <li>¿Soporta <strong>varias sucursales</strong>? ¿Con el mismo RFC o distintos?</li>
    <li>¿El cliente necesita <strong>registrarse o bajar una app</strong>? (Cada paso extra = menos facturas completadas y más reclamos en caja.)</li>
    <li>¿Puedo <strong>probar gratis</strong> antes de contratar?</li>
  </ul>

